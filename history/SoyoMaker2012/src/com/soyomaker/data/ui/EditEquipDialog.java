    private void setEquip(Equip equip) {
        this.equip = equip;
        equipNameTextField.setText(equip.name);
        equipIntroTextArea.setText(equip.intro);
        for (int i = 0; i < equipIcons.length; i++) {
            if (equip.icon.equals(equipIcons[i].getDescription())) {
                equipIconComboBox.setSelectedItem(equipIcons[i]);
                break;
            }
        }
        equipKindComboBox.setSelectedIndex(equip.equipType);
        equipLevTextField.setText(equip.lev + "");
        equipUserAniComboBox.setSelectedItem((Animation) AppData.getInstance().getCurProject().getAnimation(equip.userAniIndex));
        equipTargetAniComboBox.setSelectedItem((Animation) AppData.getInstance().getCurProject().getAnimation(equip.targetAniIndex));
        equipPriceTextField.setText(equip.price + "");
    }

