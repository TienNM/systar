    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        equip.name = equipNameTextField.getText();
        equip.intro = equipIntroTextArea.getText();
        equip.icon = equipIconComboBox.getSelectedItem().toString();
        equip.lev = Integer.parseInt(equipLevTextField.getText());
        equip.equipType = equipKindComboBox.getSelectedIndex();
        equip.price = Integer.parseInt(equipPriceTextField.getText());
        if (equipUserAniComboBox.getSelectedItem() == null || equipUserAniComboBox.getSelectedItem().toString().equals("")) {
            equip.userAniIndex = -1;
        } else {
            equip.userAniIndex = ((Animation) equipUserAniComboBox.getSelectedItem()).getIndex();
        }
        if (equipTargetAniComboBox.getSelectedItem() == null || equipTargetAniComboBox.getSelectedItem().toString().equals("")) {
            equip.targetAniIndex = -1;
        } else {
            equip.targetAniIndex = ((Animation) equipTargetAniComboBox.getSelectedItem()).getIndex();
        }
        dispose();
}//GEN-LAST:event_okButtonActionPerformed

