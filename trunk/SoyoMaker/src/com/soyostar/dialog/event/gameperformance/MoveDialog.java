    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        byte[] move = new byte[moveTable.getRowCount()];
        for (int i = 0; i < moveTable.getRowCount(); i++) {
            if (moveTM.getValueAt(i, 0).equals("向上走")) {
                move[i] = 0;
            } else if (moveTM.getValueAt(i, 0).equals("向下走")) {
                move[i] = 1;
            } else if (moveTM.getValueAt(i, 0).equals("向左走")) {
                move[i] = 2;
            } else if (moveTM.getValueAt(i, 0).equals("向右走")) {
                move[i] = 3;
            }
        }
        StringBuilder sb = new StringBuilder();
        sb.append("byte 移动路线[] = {");
        for (int i = 0; i < move.length; i++) {
            if (i == move.length - 1) {
                sb.append(move[i]);
            } else {
                sb.append(move[i]).append(",");
            }
        }
        sb.append("};");
        sd.insertScriptData(sd.npcPane.eventTable.getSelectedRow(),
            "forceMove(-1," + "移动路线);");
        sd.insertScriptData(sd.npcPane.eventTable.getSelectedRow(),
            sb.toString());
        this.dispose();
    }//GEN-LAST:event_okButtonActionPerformed
